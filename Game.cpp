    void Update() {
        Camera camera(STAGE_W, STAGE_H, BLOCK_SIZE, SCREEN_W);
        
        // calculate camera position
        camera.SetPlayerPosition(gPPlayer->GetPosition());

        gPPlayer->SetGears(gUi.GetGears());
     
        // update
        StatusData statusData;
        statusData.coin = gPPlayer->GetCoin();
        statusData.hp = gPPlayer->GetHP();

        if(!gUi.IsMenu()) {
            gObjectList.Update(&gOctGame, &camera);
        }

        ITEM_ID item = gPPlayer->PopItem();
        while(item != ITEM_ID::ITEM_ID_NONE) {
            gUi.AddItem(item);
            item = gPPlayer->PopItem();
        }

        gStage.CheckHitBlock(gObjectList);
        gStage.Draw(&gOctGame, &camera);
        gUi.Update(&gOctGame, statusData);
        gOctGame.Update();
    }

    void idle() {
        glutPostRedisplay();
    }

    void display() {
        if(gSysInf.countTime >= FrameTime) {
            glClear(GL_COLOR_BUFFER_BIT);
            gOctGame.ClearScreen();
            Update();
            gSysInf.countTime -= FrameTime;
            gOctGame.ScreenSwap();
        }
        gSysInf.countTime += clock() - gSysInf.lastTime;
        gSysInf.lastTime = clock();
    }

    void resize(int w, int h) {
        glViewport(0, 0, w, h);

        glMatrixMode(GL_PROJECTION);
        glLoadIdentity();
        gluPerspective(30.0, (double)w / (double)h, 1.0, 100.0);
        
        glMatrixMode(GL_MODELVIEW);
    }

